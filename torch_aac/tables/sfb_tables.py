# Scalefactor band offsets for SHORT windows (128 spectral lines per window).
# Source: FFmpeg's ff_swb_offset_128[] / ISO 14496-3 Table 4.86.
# Key: sample rate (Hz) → list of cumulative offsets ending at 128.
